# Define a list of favorite pizza types
favorite_pizzas = ['Chezious', 'Pizza Base', 'Pizza Hut']

# Print the names of each pizza using a for loop
print("Pizza names:")
for pizza in favorite_pizzas:
    print(pizza)

# Print a sentence for each pizza type using a for loop
print("\nPizza statements:")
for pizza in favorite_pizzas:
    print("I like " + pizza + " pizza.")

# Print a summary statement outside the loop
print("\nPizza is one of my favorite foods.")
print("I especially love Chezious, Pizza Base, and Pizza Hut.")
print("Pizza is just so delicious and versatile! I could eat it every day.")
