animals = ['Lion', 'Tiger', 'Bear']

# Print the names of each animal using a for loop
print("Animal names:")
for animal in animals:
    print(animal)
    
# Print a sentence for each animal type using a for loop
print("\nAnimal statements:")
for animal in animals:
    print(animal + " is a brave animal.")
    

# Print a summary statement outside the loop
print("\nThey are symbols for braveness in our society.")

    