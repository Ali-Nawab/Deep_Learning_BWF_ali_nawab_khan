places = ['Kabul', 'London', 'Switzerland', 'New York', 'Paris', 'Tokyo', 'Sydney', 'Moscow', 'Beijing', 'Rome']
print(places) # Original list
print(sorted(places)) # Temporary sorted list
print(places) # Original list
print(sorted(places, reverse=True)) # Temporary sorted list
print(places) # Original list
places.reverse() # Reverse the list
print(places) # Original list
places.reverse() # Reverse the list
print(places) # Original list
places.sort() # Sort the list
print(places) # Original list
places.sort(reverse=True) # Sort the list
print(places) # Original list
